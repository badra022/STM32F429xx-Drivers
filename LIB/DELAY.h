#ifndef _DELAY_H_
#define _DELAY_H_

inline void Delay_ms(u32 copy_u32Ticks)
{
	u64 Local_freq = 8;
	for(u64 i = 0 ; i <= copy_u32Ticks * 1000 * Local_freq ; i++)
	{
		asm("NOP");
	}
}

#endif
